// Numbers are whole, decimal, positive, negative

// Addition(+)
console.log(2+3) // 5
console.log(2.0+3) // 5
console.log(2+3.0) // 5
console.log(2.0+3.0) // 5
// Substraction(-)
console.log(2-3) // -1
console.log(2.0-3) // -1
console.log(2-3.0) // -1
console.log(2.0-3.0) // -1
// Multiplication(*)
console.log(2*3) // 6
console.log(2.0*3) // 6
console.log(2*3.0) // 6
console.log(2.0*3.0) // 6
// Division(/)
console.log(2/3) // 0.6666666666666666
console.log(2.0/3) // 0.6666666666666666
console.log(2/3.0) // 0.6666666666666666
console.log(2.0/3.0) // 0.6666666666666666
// Reminder(%)
console.log(2%3) // 2
console.log(2.0%3) // 2
console.log(2%3.0) // 2
console.log(2.0%3.0) // 2
// Power(**)
console.log(2**3) // 8
console.log(2.0**3) // 8
console.log(2**3.0) // 8
console.log(2.0**3.0) // 8

// Precidence
console.log(1.5 + 1.5 * 2) // 4.5 NOT 6
console.log((10 % 6) ** 2) // 16
console.log(200 + 0/0) // NaN NOT 200